use std::path::{Path, PathBuf};

use anyhow::Result;

fn default_output_path() -> PathBuf {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    manifest_dir
        .join("../../beeml-web/src/beeml.generated.ts")
        .canonicalize()
        .unwrap_or_else(|_| manifest_dir.join("../../beeml-web/src/beeml.generated.ts"))
}

fn resolve_output_path() -> PathBuf {
    let mut args = std::env::args().skip(1);
    if let Some(path) = args.next() {
        let path = PathBuf::from(path);
        if path.is_absolute() {
            return path;
        }
        return std::env::current_dir()
            .unwrap_or_else(|_| PathBuf::from("."))
            .join(path);
    }
    default_output_path()
}

fn ensure_parent_dir(path: &Path) -> Result<()> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent)?;
    }
    Ok(())
}

fn main() -> Result<()> {
    let out_path = resolve_output_path();
    ensure_parent_dir(&out_path)?;

    let service = beeml::rpc::bee_ml_service_descriptor();
    let mut code = vox_codegen::targets::typescript::generate_service(service);
    code.insert_str(
        0,
        "/* eslint-disable */\n// Generated by `cargo run -p beeml --bin gen-ts-api`\n\n",
    );

    std::fs::write(&out_path, code)?;
    eprintln!("generated {}", out_path.display());
    Ok(())
}
